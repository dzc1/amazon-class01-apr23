// Las directivas en Vue son instrucciones especiales que se utilizan para manipular el DOM (Document Object Model) y aplicar comportamientos específicos en los elementos de una aplicación Vue. Son atributos especiales que se añaden a las etiquetas HTML y que comienzan con el prefijo "v-".

// Existen diferentes tipos de directivas en Vue, como por ejemplo:

// v-if: Permite renderizar o no un elemento HTML basado en una expresión condicional. Si la expresión es verdadera, el elemento se mostrará, de lo contrario, se ocultará.

