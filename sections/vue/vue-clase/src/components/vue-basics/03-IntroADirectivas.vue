// v-bind: Se utiliza para enlazar atributos HTML a valores dinámicos en el componente Vue. Por ejemplo, puedes enlazar el atributo "src" de una etiqueta "img" a una propiedad del componente.

// v-on: Permite escuchar eventos en el DOM y ejecutar métodos en respuesta a esos eventos. Por ejemplo, puedes utilizar v-on para ejecutar una función cuando se haga clic en un botón.

// v-model: Proporciona enlace bidireccional entre una propiedad de datos y un elemento de formulario. Es útil para manejar la entrada del usuario en formularios.

// Estas directivas se utilizan para extender la funcionalidad de Vue y permitir la creación de aplicaciones interactivas y dinámicas.Al utilizar directivas, podemos controlar el comportamiento y la apariencia de los elementos HTML de forma declarativa, lo que facilita la manipulación y actualización del DOM en respuesta a los cambios en los datos o eventos de la aplicación.
